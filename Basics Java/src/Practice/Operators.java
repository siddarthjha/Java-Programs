class Operators
{
	public static void main(String[] args)
	{
		
		int x = 10, y = 15;
		System.out.println("x="+ x);
		System.out.println("x++="+ x++);
		System.out.println("++x="+ ++x);
		System.out.println(x++ + ++x);
		System.out.println("~x="+ ~x);
		System.out.println(x + y);
		System.out.println(x * y);
		System.out.println(x - y);
		System.out.println(x / y);
		System.out.println(x % y);
		
		
	}
}

